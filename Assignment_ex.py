# Assignment Operator
number = int(input("Enter number "))
number+= 10
print("+= 10 is {}" .format(number))
number-= 10
print("-= 10 is {}" .format(number))
number*= 10
print("*= 10 is {}" .format(number))
number/= 10
print("/= 10 is {}" .format(number))

number//= 10
print("//= 10 is {}" .format(number))
number%= 10
print("%= 10 is {}" .format(number))
number**= 10
print("**= 10 is {}" .format(number))

